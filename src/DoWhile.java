public class DoWhile {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //deklarasi variabel//
        int bil = 1;
        
        //perulangan//
        do{
            
            //pernyataan//
            System.out.print(bil+"  ");
            bil++;
        
        //rumus batas perulangan//
        }while (bil<=10);
    }
    
}
